const nomeDePaises = ['Brasil', 'Austrália', 'Grécia', 'Alemanha', 'Estados Unidos'];
//adicione um país ao final da lista
nomeDePaises.push('Inglaterra');
//imprima a lista na tela
console.log(nomeDePaises);
//remova um país do fim da lista
nomeDePaises.pop();
//imprima a lista na tela
console.log(nomeDePaises);
//adicione um país ao início da lista
nomeDePaises.unshift('Espanha');
//imprima a lista na tela
console.log(nomeDePaises);
//remova um país do inicio da lista
nomeDePaises.shift();
//imprima a lista na tela
console.log(nomeDePaises);

//imprima o ultimo país da lista na tela
